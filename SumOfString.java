public class SumOfString {
  public static void main(String[] args) {
    String s1 = "111";
    String s2 = "222";
    String s3 = "333";
    int sum = Integer.parseInt(s1) + Integer.parseInt(s2) + Integer.parseInt(s3);
    System.out.println(sum);
    System.out.println(String.valueOf(sum));

  }
}
